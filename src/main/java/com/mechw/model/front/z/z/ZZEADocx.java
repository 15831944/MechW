package com.mechw.model.front.z.z;

public class ZZEADocx {

    private String ribbonName;

    private Double dbmm;
    private Double hmm;
    private Double dsmm;
    private Double db;
    private Double h;
    private Double ds;
    private Double an;
    private Double g;
    private Double t;

    public ZZEADocx() {
    }

    public ZZEADocx(String ribbonName, Double dbmm, Double hmm, Double dsmm, Double db, Double h, Double ds, Double an, Double g, Double t) {
        this.ribbonName = ribbonName;
        this.dbmm = dbmm;
        this.hmm = hmm;
        this.dsmm = dsmm;
        this.db = db;
        this.h = h;
        this.ds = ds;
        this.an = an;
        this.g = g;
        this.t = t;
    }

    public String getRibbonName() {
        return ribbonName;
    }

    public void setRibbonName(String ribbonName) {
        this.ribbonName = ribbonName;
    }

    public Double getDbmm() {
        return dbmm;
    }

    public void setDbmm(Double dbmm) {
        this.dbmm = dbmm;
    }

    public Double getHmm() {
        return hmm;
    }

    public void setHmm(Double hmm) {
        this.hmm = hmm;
    }

    public Double getDsmm() {
        return dsmm;
    }

    public void setDsmm(Double dsmm) {
        this.dsmm = dsmm;
    }

    public Double getDb() {
        return db;
    }

    public void setDb(Double db) {
        this.db = db;
    }

    public Double getH() {
        return h;
    }

    public void setH(Double h) {
        this.h = h;
    }

    public Double getDs() {
        return ds;
    }

    public void setDs(Double ds) {
        this.ds = ds;
    }

    public Double getAn() {
        return an;
    }

    public void setAn(Double an) {
        this.an = an;
    }

    public Double getG() {
        return g;
    }

    public void setG(Double g) {
        this.g = g;
    }

    public Double getT() {
        return t;
    }

    public void setT(Double t) {
        this.t = t;
    }
}
