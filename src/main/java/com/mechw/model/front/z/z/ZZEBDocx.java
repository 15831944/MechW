package com.mechw.model.front.z.z;

public class ZZEBDocx {

    private String ribbonName;

    private Double alpha;
    private Double hmm;
    private Double dmm;
    private Double theta;
    private Double h;
    private Double d;
    private Double an;
    private Double g;
    private Double t;

    public ZZEBDocx() {
    }

    public ZZEBDocx(String ribbonName, Double alpha, Double hmm, Double dmm, Double theta, Double h, Double d, Double an, Double g, Double t) {
        this.ribbonName = ribbonName;
        this.alpha = alpha;
        this.hmm = hmm;
        this.dmm = dmm;
        this.theta = theta;
        this.h = h;
        this.d = d;
        this.an = an;
        this.g = g;
        this.t = t;
    }

    public String getRibbonName() {
        return ribbonName;
    }

    public void setRibbonName(String ribbonName) {
        this.ribbonName = ribbonName;
    }

    public Double getAlpha() {
        return alpha;
    }

    public void setAlpha(Double alpha) {
        this.alpha = alpha;
    }

    public Double getHmm() {
        return hmm;
    }

    public void setHmm(Double hmm) {
        this.hmm = hmm;
    }

    public Double getDmm() {
        return dmm;
    }

    public void setDmm(Double dmm) {
        this.dmm = dmm;
    }

    public Double getTheta() {
        return theta;
    }

    public void setTheta(Double theta) {
        this.theta = theta;
    }

    public Double getH() {
        return h;
    }

    public void setH(Double h) {
        this.h = h;
    }

    public Double getD() {
        return d;
    }

    public void setD(Double d) {
        this.d = d;
    }

    public Double getAn() {
        return an;
    }

    public void setAn(Double an) {
        this.an = an;
    }

    public Double getG() {
        return g;
    }

    public void setG(Double g) {
        this.g = g;
    }

    public Double getT() {
        return t;
    }

    public void setT(Double t) {
        this.t = t;
    }
}
