package com.mechw.model.db.nbt4700312009.property.stress;

public class StressResult {

    private Double o;
    private Double ot;

    public StressResult() {
    }

    public StressResult(Double o, Double ot) {
        this.o = o;
        this.ot = ot;
    }

    public Double getO() {
        return o;
    }

    public void setO(Double o) {
        this.o = o;
    }

    public Double getOt() {
        return ot;
    }

    public void setOt(Double ot) {
        this.ot = ot;
    }
}
